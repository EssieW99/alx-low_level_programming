#include "3-calc.h"

/**
 * op_add - Returns sum of a and b
 * @a: One of the integers
 * @b: The other number
 *
 * Return: Value of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_add - Calculates difference of a and b
 * @a: One of the numbers
 * @b: The other number
 *
 * Return: Is the value of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_sub - Multiplies a and b
 * @a: One of the numbers
 * @b: The other number
 *
 * Return: Is the value of a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Divides a by b
 * @a: One of the numbers
 * @b: The other number
 *
 * Return: The value of a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Calculates the remainder of the division of a by b
 * @a: One of the numbers
 * @b: The other number
 *
 * Return: Is the value of a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
